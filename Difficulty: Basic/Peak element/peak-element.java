class Solution {
    // Function to find the peak element
    public int peakElement(int[] arr, int n) {
        // If there's only one element, it's the peak
        if (n == 1) {
            return 0;
        }

        // Check if the first element is a peak
        if (arr[0] >= arr[1]) {
            return 0;
        }

        // Check if the last element is a peak
        if (arr[n - 1] >= arr[n - 2]) {
            return n - 1;
        }

        // Check for peak elements in the middle of the array
        for (int i = 1; i < n - 1; i++) {
            if (arr[i] >= arr[i - 1] && arr[i] >= arr[i + 1]) {
                return i;  // Return the index of the peak element
            }
        }

        return -1;  // This case will never be reached due to the problem constraints
    }
}
